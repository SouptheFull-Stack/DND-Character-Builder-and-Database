const { Model, DataTypes } = require("sequelize");
const sequelize = require("../config/connection");

class Alignment extends Model {}

Alignment.init(
  {
    id: {
      type: DataTypes.INTEGER,
      primaryKey: true,
      autoIncrement: true,
    },
    name: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    description: {
      type: DataTypes.TEXT,
      allowNull: false,
    },
  },
  {
    sequelize,
    timestamps: true,
    freezeTableName: true,
    underscored: true,
    modelName: "alignment",
  }
);

module.exports = Alignment;
